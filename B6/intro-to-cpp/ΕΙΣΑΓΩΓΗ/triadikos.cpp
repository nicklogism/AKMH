/* Μεγιστοποίησε τη σαφήνεια,
αντί να ελαχιστοποιείς τους χαρακτήρες που γράφεις*/

#include <iostream>


int main()
{

  int x=1,y=3;

  std::cout << (x < y) ? 0 : 1; // ασαφής - άσχημος αλλά λιγότερος κώδικας

  // Σαφής αλλά περισσότερος κώδικας
  if(x<y)
    std::cout << 1;
  else
    std::cout << 0;

  return 0;
}
